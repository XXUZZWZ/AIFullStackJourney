// 添加推荐功能
document.addEventListener('DOMContentLoaded', () => {
    const recommendBtn = document.getElementById('recommendBtn');
    const resultDiv = document.getElementById('result');

    let wishlist = JSON.parse(localStorage.getItem('wishlist')) || [];

    function updateWishlistDisplay() {
        const wishlistDiv = document.getElementById('wishlist');
        wishlistDiv.innerHTML = `<h3>想吃清单（${wishlist.length}）</h3>` + 
            wishlist.map((item, index) => `
                <div class="wishlist-item">
                    <span>${item.名称}（${item.类型}）</span>
                    <button class="remove-btn" data-index="${index}">×</button>
                </div>
            `).join('');
    }

    document.addEventListener('click', (e) => {
        if (e.target.classList.contains('remove-btn')) {
            const index = parseInt(e.target.dataset.index);
            wishlist.splice(index, 1);
            localStorage.setItem('wishlist', JSON.stringify(wishlist));
            updateWishlistDisplay();
        }
    });

    // 修改原点击事件
    recommendBtn.addEventListener('click', () => {
        const randomIndex = Math.floor(Math.random() * foods.length);
        const selected = foods[randomIndex];
        
        recommendBtn.style.transform = 'scale(0.95)';
        setTimeout(() => {
            recommendBtn.style.transform = 'scale(1)';
        }, 100);

        resultDiv.innerHTML = `
            <div class="food-card">
                <h2>今日推荐：${selected.名称}</h2>
                <p>类型：${selected.类型}</p>
            </div>
        `;

        document.querySelector('.add-btn').addEventListener('click', () => {
            if (!wishlist.some(item => item.名称 === selected.名称)) {
                wishlist.push(selected);
                localStorage.setItem('wishlist', JSON.stringify(wishlist));
                updateWishlistDisplay();
            }
        });
    });

    // 初始化显示
    updateWishlistDisplay();
});
